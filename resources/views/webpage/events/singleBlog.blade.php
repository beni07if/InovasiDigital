              <div class="entry-content">
                <p>The EU Deforestation Regulation (EUDR) has been a prominent issue facing many operators across several key commodities who seek to trade them into and out of the European Union (EU). Palm oil is one such commodity, and the EUDR aims to stop deforestation-related commodities from entering supply chains linked to operators in the EU.</p>

                <p>On 20th November 2023, Inovasi Digital and MosaiX held an event titled “Navigating EUDR Requirements: Solutions for Effective Implementation” at Black Pond Tavern, in Jakarta. It was hosted by Inovasi Digital and attracted over 60 participants from various companies and organizations concerned with addressing the challenges associated with EUDR implementation.</p>

                <p>The event included presentations by Eddy Esselink from the Netherlands Oils and Fats Organization (MVO), who shared the views and concerns of operators, as well as Andrew Ng from MosaiX BV, and Ihwan Rafina from PT Inovasi Digital, who identified and explained the critical EUDR requirements through their presentation titled “EUDR Un-Redacted”. A discussion on the topic followed, which was moderated by Sara Wayne.</p>

                <p>The moderator began the session by framing the core requirements of the EUDR for the audience. That included revisiting the evolution of the EU laws and initiatives that led to the adoption of the EUDR. Then she briefly touched upon the demands of the EUDR itself and identified the deforestation as well as legality components</p>

                <p>The event was divided into two sessions, the first session included presentations and discussions on the challenges posed by the EUDR and the solutions developed by Inovasi Digital and MosaiX, and the second session was a cocktail event designed to facilitate the building and expansion of professional networks in a relaxed and informal setting. During the first session, the speakers delved into the current state of the palm oil sector, the challenges arising from the newly enforced EU regulation, and the solution developed by MosaiX called Inovasi Agriplot.</p>

                <p>Eddy Esselink started the session by explaining that there were many unanswered questions, indicating several aspects of the regulations that remained unclear. He proposed for harmonization in the implementation of the EUDR across jurisdictions. Two specific aspects were highlighted: the need for clarity on the required documents related to legal compliance and the importance of harmonizing information for risk assessments. Eddy called for efforts to engage with governments through a coordinated response.</p>

                <p>The second speaker was Andrew Ng from MosaiX. Andrew covered the various requirements and potential approaches to address the EUDR. The presentation touched on the complexities of gathering verifiable and detailed information. Several approaches were then proposed with an emphasis on the critical need for reliable data sources.</p>

                <p>The last presenter was Ihwan Rafina from Inovasi Digital who expanded on how to address the challenges with an approach used by ID’s InovasiAgriplot system. Ihwan expanded on the types of approaches to determining mapping requirements, deforestation assessments, legal compliance, and closing of traceability gaps. Challenges due to weak points for information gathering include the supplier mills’ information, accessing legal documents, inconsistencies between official and physical data, and other technical complexities explained. Broader issues like different international and national forest definitions were discussed as well.</p>

                <p>Ihwan compared certification information disclosure (like RSPO / MSPO) to EUDR requirements and highlighted that potential shortfalls existed that meant considering certification as compliant to EUDR cannot be assumed. Then the presentation showcased the extensive data collected by Inovasi Digital and Mosaix, which would be critical for mapping, traceability, legality, and risk assessments. The extensive data collected across different EUDR-required criteria ensures InovasiAgriplot system would identify compliant supply chains with greater accuracy. The expertise in combining Big-Data, analytics with on-the-ground experience was highlighted as the system’s main risk assessment strength.</p>

                <p>The session was followed by a networking event where the conversation continued amongst various operators along the supply chain.</p>

